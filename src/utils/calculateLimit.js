export function calculateSavings(donationAmount) {
  const returnValue = Math.round(donationAmount * 0.3);
  const adminFee = 2000;
  const effectiveSavings = returnValue - adminFee;
  const taxBenefit = donationAmount - adminFee;
  return { returnValue, adminFee, effectiveSavings, taxBenefit };
}
